

public class Arithmetic {
    public static void main(String[] args) {
        int x = 10 , y = 3;
        System.out.println("x=10 and y=3");
        System.out.println("x+y = " + (x+y));
        System.out.println("x-y= " + (x-y));
        System.out.println("x*y = " + (x*y));
        System.out.println("x/y = " + (x/y));
        System.out.println("x%y = " + (x%y)); 

    }
    
}
