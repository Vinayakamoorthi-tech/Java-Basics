public class Bitwise {
    public static void main(String[] args) {
        int a=10, b=2;
        System.out.println("a=10 , b=2");
        System.out.println("a&b = " + (a&b));
        System.out.println("a|b = " + (a|b));
        System.out.println("a^b = " + (a^b));
        System.out.println("~a = " + (~a));
        System.out.println("a<<b = " + (a<<b));
        System.out.println("a>>b = " + (a>>b));
        System.out.println("a>>>b = " + (a>>>b));

    }    
}
