

public class Relational {
    public static void main(String[] args) {
        int a=6, b=8;
        System.out.println("a=6 , b=8");
        System.out.println("a==b = " + (a==b));
        System.out.println("a!=b = " + (a!=b));
        System.out.println("a>b = " + (a>b));
        System.out.println("a<b = " + (a<b));
        System.out.println("a>=b = " + (a>=b));
        System.out.println("a<=b = " + (a<=b));
    }
    
}
