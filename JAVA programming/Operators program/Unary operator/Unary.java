

public class Unary{
    public static void main(String[] args) {
        int a=10;
        boolean b=true, c=false;
        System.out.println("a=10 , b=true , c=false");
        System.out.println("+a = " + (+a));
        System.out.println("-a = " + (-a));
        System.out.println("++a = " + (++a));
        System.out.println("--a = " + (--a));
        System.out.println("~a = " + (~a));
        System.out.println("!b = " + (!b));
        System.out.println("!c = " + (!c));
    }
}
